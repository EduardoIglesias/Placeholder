import UIKit

extension UIColor {

    class var lightgrey: UIColor {
        return UIColor(red: 249.0 / 255.0, green: 249.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
    }
    
    class var grey: UIColor {
      return UIColor(white: 155.0 / 255.0, alpha: 1.0)
    }
    
    class var darkgrey: UIColor {
      return UIColor(white: 45.0 / 255.0, alpha: 1.0)
    }
    
    class var validategreen: UIColor {
        return UIColor(red: 39.0 / 255.0, green: 195.0 / 255.0, blue: 76.0 / 255.0, alpha: 1.0)
    }
    
}
